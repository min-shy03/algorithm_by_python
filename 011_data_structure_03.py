# 예시로 들어볼게:

# 친구들 전화번호를 메모장에 아무렇게나 적어두면 찾기 힘들잖아?

# 근데 이름 순서대로 정리하면 금방 찾을 수 있지!

# 이런 "정리하는 방식"이 바로 자료구조야.

# 데이터가 막 흩어져 있으면 쓸 때 힘드니까,
# 어떻게 모을까? 어떻게 연결할까?
# 이런 걸 고민해서 정리해둔 틀이 자료구조.


# 📌 왜 알아야 돼?
# 컴퓨터가 일을 쉽게, 빠르고, 적게 힘쓰면서 하게 만들려고!

# 생각해봐:

