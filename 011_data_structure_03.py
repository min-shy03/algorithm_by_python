# 순식간에 찾을 수 있어!

# 빠른 검색, 효율적인 저장, 빠른 삭제
# 이런 걸 위해 자료구조가 필요해.


# 📌 리스트, 튜플도 자료구조야?
# 맞아! 파이썬에서 쓰는 리스트([])랑 튜플(())도
# 데이터들을 줄 세워서 모아둔 방식이니까,
# 이걸 **"배열(Array)"**이라고 부르기도 해.

